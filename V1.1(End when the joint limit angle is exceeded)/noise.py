import numpy as np
import config

class OUNoise:
    def __init__(self, action_dimension, mu=config.NOISE_MU, theta=config.NOISE_THETA, sigma=config.NOISE_SIGMA):
        self.action_dimension = action_dimension
        self.mu = mu
        self.theta = theta
        self.sigma = sigma
        self.state = np.ones(self.action_dimension) * self.mu
        self.reset()

    def reset(self):
        self.state = np.ones(self.action_dimension) * self.mu

    def sample(self):
        x = self.state
        dx = self.theta * (self.mu - x) + self.sigma * np.random.randn(len(x))
        self.state = x + dx
        return self.state